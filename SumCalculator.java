// Task 1: Program to calculate the sum of two numbers
public class SumCalculator {
    public static void main(String[] args) {
        System.out.println("This program has calculated the sum of two numbers:");
        int num1 = 9;
        int num2 = 67;
        int sum = num1 + num2;
        System.out.println(num1 + " + " + num2 + " = " + sum);
    }
}
